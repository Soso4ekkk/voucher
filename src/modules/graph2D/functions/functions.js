//упрощалки для ввода функций в graph2D
window.sin = x => Math.sin(x);
window.cos = x => Math.cos(x);
window.tg = x => Math.tan(x);
window.ctg = x => 1 / Math.tan(x);
window.pow = (x, n) => Math.pow(x, n);
window.asin = (x) => Math.asin(x);
window.acos = (x) => Math.acos(x);
window.atg = (x) => Math.atan(x);
window.actg = (x) => 1 / Math.atan(x);

export default window;